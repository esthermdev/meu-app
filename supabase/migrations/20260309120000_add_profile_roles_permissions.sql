-- Ensure each profile has exactly one primary role assignment
with missing_primary as (
  select pr.profile_id, min(pr.role_id) as role_id
  from public.profile_roles pr
  group by pr.profile_id
  having bool_or(pr.is_primary) = false
)
update public.profile_roles pr
set is_primary = true
from missing_primary mp
where pr.profile_id = mp.profile_id
  and pr.role_id = mp.role_id;

create or replace function public.sync_profile_roles_primary_from_profile()
returns trigger
language plpgsql
as $$
begin
  if new.role_id is null then
    return new;
  end if;

  update public.profile_roles
  set is_primary = false
  where profile_id = new.id;

  insert into public.profile_roles (profile_id, role_id, is_primary)
  values (new.id, new.role_id, true)
  on conflict (profile_id, role_id)
  do update set is_primary = true;

  return new;
end;
$$;

-- Keep profile_roles primary assignment in sync with profiles.role_id
drop trigger if exists trg_profiles_sync_primary_profile_role on public.profiles;

create trigger trg_profiles_sync_primary_profile_role
after insert or update of role_id on public.profiles
for each row
execute function public.sync_profile_roles_primary_from_profile();

create or replace function public.get_profile_access(p_profile_id uuid)
returns table(role_keys text[], permission_keys text[])
language sql
stable
as $$
  with assigned_roles as (
    select distinct r.key
    from public.profile_roles pr
    join public.roles r on r.id = pr.role_id
    where pr.profile_id = p_profile_id
  ),
  assigned_permissions as (
    select distinct p.key
    from public.profile_roles pr
    join public.role_permissions rp on rp.role_id = pr.role_id
    join public.permissions p on p.id = rp.permission_id
    where pr.profile_id = p_profile_id
  )
  select
    coalesce((select array_agg(ar.key order by ar.key) from assigned_roles ar), '{}'::text[]) as role_keys,
    coalesce((select array_agg(ap.key order by ap.key) from assigned_permissions ap), '{}'::text[]) as permission_keys;
$$;

grant execute on function public.get_profile_access(uuid) to anon, authenticated;

