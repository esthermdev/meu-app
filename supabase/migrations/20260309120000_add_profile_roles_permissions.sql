-- Multi-role access model with permissions
-- Supports users having multiple roles (e.g., driver + volunteer)

create table if not exists public.permissions (
  id smallint generated by default as identity primary key,
  key text not null unique,
  name text not null,
  description text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.role_permissions (
  role_id smallint not null references public.roles(id) on delete cascade,
  permission_id smallint not null references public.permissions(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (role_id, permission_id)
);

create table if not exists public.profile_roles (
  profile_id uuid not null references public.profiles(id) on delete cascade,
  role_id smallint not null references public.roles(id) on delete cascade,
  is_primary boolean not null default false,
  created_at timestamptz not null default now(),
  primary key (profile_id, role_id)
);

create unique index if not exists profile_roles_one_primary_idx
  on public.profile_roles(profile_id)
  where is_primary;

