  description = excluded.description,
  updated_at = now();

insert into public.role_permissions (role_id, permission_id)
select r.id, p.id
from public.roles r
join public.permissions p on (
  (r.key = 'admin')
  or (r.key = 'driver' and p.key in ('manage_transport', 'receive_cart_notifications'))
  or (r.key = 'volunteer' and p.key in ('manage_water', 'receive_water_notifications'))
  or (r.key = 'medic' and p.key in ('manage_trainer_requests', 'receive_medic_notifications'))
)
on conflict (role_id, permission_id) do nothing;

-- Backfill primary role from existing profiles.role_id
insert into public.profile_roles (profile_id, role_id, is_primary)
select p.id, p.role_id, true
from public.profiles p
where p.role_id is not null
on conflict (profile_id, role_id)
do update set is_primary = excluded.is_primary;

-- Backfill additional roles from legacy flags
